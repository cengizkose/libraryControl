    public void kitapTeslimAl(int ogrNo){
        try{
            Statement st=lwn.dbOpen();
            st.executeUpdate("UPDATE out_book OB, book B SET B.k_durum = 0 WHERE OB.ogrenci_no= '"+ogrNo+"' AND OB.kitapno=B.kitap_no"); 
                lwn.dbClose();            
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }
    
     public void ogrenciSil(int ogrNo){
         try{
             Statement st=lwn.dbOpen();
             st.executeUpdate("DELETE FROM student WHERE ogrenci_no ='"+ogrNo+"' ");
             lwn.dbClose();
         }catch (Exception e){
             JOptionPane.showMessageDialog(null,e.getMessage());
         }
     }

     public void tabloDoldur2(){ 
        try{
            Statement st=lwn.dbOpen();
            res=st.executeQuery("SELECT * FROM student");
                 while(res.next()){
                      model2.insertRow(model2.getRowCount(), new Object[]{res.getInt("ogrenci_no"),res.getString("ogrenci_ad"),res.getString("ogrenci_soyad"),res.getString("ogrenci_sinif")});
                 }
            lwn.dbClose();
            }catch (Exception e) {
                JOptionPane.showMessageDialog(null,e.getMessage());
            }
    }
    
