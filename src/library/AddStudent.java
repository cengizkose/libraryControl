    private void ogrEkleButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ogrEkleButonActionPerformed
        
        String snf = sinifSec();
        if (!"".equals(ogrNo.getText()) && !"".equals(ogrAd.getText()) && !"".equals(ogrSoyad.getText()) && !"".equals(snf) ){
            try{
                Statement st=lwn.dbOpen();
                st.executeUpdate("insert into student" + " values ('"+Integer.parseInt(ogrNo.getText())+"','"+ogrAd.getText()+"','"+ogrSoyad.getText()+"','"+snf+"')");
                JOptionPane.showMessageDialog(rootPane,"Kayıt Tamamlandı");
                ogrNo.setText("");
                ogrAd.setText("");
                ogrSoyad.setText("");
                lwn.dbClose();
            }catch (Exception e) {
                JOptionPane.showMessageDialog(null,e.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null,"Öğrenci Bilgilerinde Eksik var !","Hata",JOptionPane.OK_OPTION);
        }
        tabloYenile();
    }//GEN-LAST:event_ogrEkleButonActionPerformed

