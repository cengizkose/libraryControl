    public void tabloDoldur3(){
        
        try{
            Statement st=lwn.dbOpen();
            res=st.executeQuery("SELECT S.*, B.kitap_no, B.kitap_ad  FROM student S, out_book OB , book B WHERE OB.ogrenci_no = S.ogrenci_no AND OB.kitapno=B.kitap_no");
                 while(res.next()){
                     model3.insertRow(model3.getRowCount(), new Object[]{res.getInt("ogrenci_no"),res.getString("ogrenci_ad"),res.getString("ogrenci_soyad"),res.getString("ogrenci_sinif"),res.getInt("kitap_no"),res.getString("kitap_ad")});    
                 }
            lwn.dbClose();
            }catch (Exception e) {
                JOptionPane.showMessageDialog(null,e.getMessage());
            }
    }
