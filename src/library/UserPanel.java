    public void tabloDoldur(){
        
        try{
               Statement st=lwn.dbOpen();
               res=st.executeQuery("SELECT * FROM book where k_durum = 0");
                    while(res.next()){
                        model.insertRow(model.getRowCount(), new Object[]{res.getInt("kitap_no"),res.getString("kitap_ad"),res.getString("kitap_yaz")});
                    }
            lwn.dbClose();
            }catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,e.getMessage());
            }
    }
  
    public void kitapVer(){
        try{
            Statement st=lwn.dbOpen();
            st.executeUpdate("insert into out_book " + "values ( '"+Integer.parseInt(kNoText2.getText())+"','"+Integer.parseInt(ogrNoText.getText())+"','"+1+"')");
            JOptionPane.showMessageDialog(null,"Öğrenciye Kitap Verildi...");
            lwn.dbClose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage() );
        }
    }
    public void kitapDurum(int b, String c){
        try{
            Statement st=lwn.dbOpen();
            st.executeUpdate("UPDATE book SET k_durum= '"+c+"' WHERE kitap_no = '"+b+"' ");                 
            lwn.dbClose();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage() );
        }
        ogrNoText.setText("");
    }
    public void kayitSil(int a, int b){
        try {
            Statement st=lwn.dbOpen();
            st.executeUpdate("DELETE FROM out_book WHERE ogrenci_no = '"+a+"' and kitapno = '"+b+"' ");
            lwn.dbClose();
            JOptionPane.showMessageDialog(null,"Kitap Öğrenciden Alındı...");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage() );
        }
    }
    public void yenile(){
         model.setRowCount(0);
         tabloDoldur();
    }
   
