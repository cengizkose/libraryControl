    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed

        if (!"".equals(kNo.getText()) && !"".equals(kAd.getText()) && !"".equals(kYaz.getText()) ){
            try{
                Statement st=lwn.dbOpen();
                st.executeUpdate("insert into book" + " values ('"+Integer.parseInt(kNo.getText())+"','"+kAd.getText()+"','"+kYaz.getText()+"','"+0+"')");
                JOptionPane.showMessageDialog(null,"Kayıt Tamamlandı");

                lwn.dbClose();
            }catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,e.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null,"Kitap Bilgilerinde Eksik var !","Hata",JOptionPane.OK_OPTION);
        }
        model4.insertRow(model4.getRowCount(), new Object[]{Integer.parseInt(kNo.getText()),kAd.getText(),kYaz.getText()});
        textClear();
    }//GEN-LAST:event_ekleActionPerformed

