    public void tabloDoldur(){
        
        try{
               Statement st=lwn.dbOpen();
               res=st.executeQuery("SELECT * FROM book where k_durum = 0");
                    while(res.next()){
                        model4.insertRow(model4.getRowCount(), new Object[]{res.getInt("kitap_no"),res.getString("kitap_ad"),res.getString("kitap_yaz")});
                    }
            lwn.dbClose();
            }catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,e.getMessage());
            }
    }
    public void kitapSil(){
         try{
             Statement st=lwn.dbOpen();
             st.executeUpdate("DELETE FROM book WHERE kitap_no ='"+Integer.parseInt(kNo.getText())+"' ");
             lwn.dbClose();
         }catch (Exception e){
             JOptionPane.showMessageDialog(null,e.getMessage());
         }
     }
